"""Pure before/after answer computation + markdown rendering for the ER-KG demo.

No goldenmatch, no network, no erkgbench.adapters import -- so this is unit-tested
offline. A clustering is list[list[int]] over record indices; records are described
by index->mention and index->entity_id maps. run_demo.py adapts the harness output
into these inputs.
"""
from __future__ import annotations

EXACT_FAMILY_F1 = "F1 0.066"   # GraphRAG/LightRAG/Cognee/mem0 on the SCALED corpus (206 rec)
COLL_P_DET = "0.47"            # goldenmatch auto+fields collision precision (0.471, unchanged by scaling)
COLL_P_LLM = "1.0"            # goldenmatch auto+llm collision precision
# NOTE: these prose numbers are pinned to RESULTS.md. The drug-scaling moved the
# exact-family F1 0.089 -> 0.066, proving they drift -- run_demo.py SHOULD pass the
# exact-family F1 read from the freshly-generated results.json into render_demo_md
# rather than relying on this constant. (Kept as a documented fallback default.)


def complete_partition(clusters: list[list[int]], all_indices: list[int]) -> list[list[int]]:
    """Add implied singletons for indices missing from `clusters` (goldenmatch
    returns multi-member clusters only)."""
    seen = {i for c in clusters for i in c}
    out = [list(c) for c in clusters]
    out.extend([i] for i in all_indices if i not in seen)
    return out


def _entity_indices(eids: dict[int, str], entity_id: str) -> list[int]:
    return sorted(i for i, e in eids.items() if e == entity_id)


def under_merge_answer(clusters, mentions, eids, entity_id, query):
    """What the agent 'knows' about `entity_id` when it queries the string `query`.

    distinct_nodes  -- how many clusters the entity's records are spread across
                       (>1 == the KG stored one entity as several).
    names_reachable -- mentions in the cluster that the query string lands on
                       (the node whose members include a mention == query).
    complete        -- whether that reachable name set covers ALL the entity's mentions.
    """
    ent = set(_entity_indices(eids, entity_id))
    distinct_nodes = sum(1 for c in clusters if ent.intersection(c))
    landed = next((c for c in clusters if any(mentions[i] == query for i in c)), [])
    names_reachable = sorted({mentions[i] for i in landed})
    all_names = {mentions[i] for i in ent}
    complete = set(names_reachable) >= all_names
    return {
        "distinct_nodes": distinct_nodes,
        "names_reachable": names_reachable,
        "all_names": sorted(all_names),
        "complete": complete,
    }


def pair_merged(clusters, eids, id_a, id_b) -> bool:
    """True iff at least one cluster contains records from BOTH entity_ids (over-merge)."""
    for c in clusters:
        es = {eids[i] for i in c}
        if id_a in es and id_b in es:
            return True
    return False


def render_demo_md(mentions, eids, entity_id, query, before, after, exact_family_f1=EXACT_FAMILY_F1) -> str:
    """Deterministic markdown for the committed Tier-1 (under-merge) artifact.

    `exact_family_f1` is the cited exact-match-family F1 string (e.g. "F1 0.066");
    run_demo.py passes the value read from the freshly-generated results.json so the
    prose never drifts from RESULTS.md. Defaults to the module constant.
    """
    b = under_merge_answer(before, mentions, eids, entity_id, query)
    a = under_merge_answer(after, mentions, eids, entity_id, query)
    all_names = a["all_names"]
    lines = [
        "# ER-KG-Bench demo: entity resolution flips the answer",
        "",
        "_Generated by `demo/run_demo.py` from the real corpus (`dataset/records.csv`)."
        " Do not hand-edit; regenerate and commit (see the bench-er-kg CI lane)._",
        "",
        "## Under-merge: a fragmented entity gives an incomplete answer",
        "",
        f"The corpus entity `{entity_id}` has {len(all_names)} real surface forms: "
        + ", ".join(f"`{n}`" for n in all_names) + ".",
        "",
        "**Before — exact-match KG (GraphRAG / LightRAG / Cognee / mem0 family).** "
        "These resolve only byte-identical names, so each surface form becomes its own "
        f"node. Agent question: *\"How many organizations are in memory, and what are "
        f"{query}'s known names?\"*",
        "",
        f"> {b['distinct_nodes']} organizations (the entity is stored "
        f"{b['distinct_nodes']} times); names reachable from \"{query}\": "
        + ", ".join(f"`{n}`" for n in b["names_reachable"]) + ".",
        "",
        f"That is the failure ER-KG-Bench measures in aggregate: the exact-match family "
        f"scores **{exact_family_f1}** on real surface variation.",
        "",
        "**After — zero-config `goldenmatch.dedupe_df` (`auto+fields`).** Multi-field ER "
        "(name + type + context) unifies the surface forms into one entity.",
        "",
        f"> {a['distinct_nodes']} organization; names reachable from \"{query}\": "
        + ", ".join(f"`{n}`" for n in a["names_reachable"]) + ".",
        "",
        "Same documents, same query. The answer goes from a duplicated, half-blind entity "
        "to one complete entity. Behavior is the harness's own adapters over the real "
        "corpus; the only construction is treating each surface form as one ingested "
        "mention, which is exactly what real KG/agent-memory ingestion does.",
        "",
    ]
    return "\n".join(lines) + "\n"
